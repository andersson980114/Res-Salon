    private void RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarActionPerformed
        try {    
            Usuario usuario = new Usuario();
            usuario.setTipousuario(Tipo.getSelectedItem().toString());
            usuario.setNombre(Nombre.getText());
            usuario.setApellido(Apellido.getText());
            usuario.setTelefono(Telefono.getText());
            usuario.setCorreo(Correo.getText());
            usuario.setContraseña(Clave.getText());
            usuario.setCodpersonal(Integer.parseInt(codigo.getText())); 
            limpiar();
            llenarTabla();
            usuarioLogica.registrarUsuario(usuario);
            JOptionPane.showMessageDialog(this, "Usuario registrado correctamente"); 
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Usuario ya esta registrado");
        }
        llenarTabla();
    }//GEN-LAST:event_RegistrarActionPerformed

    public void llenarTabla() {
        List<Usuario> listaUsuarios = usuarioLogica.consultarUsuarios();
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.addColumn("codigo");
        modelo.addColumn("Apellido");
        modelo.addColumn("Nombre");
        modelo.addColumn("Teléfono");
        modelo.addColumn("Correo");
        String tipoUser = Tipo.getSelectedItem().toString();
        String[] datos = new String[5];
        for (Usuario u : listaUsuarios) {
            if(tipoUser.equals(u.getTipousuario())) {
                datos[0] = Integer.toString(u.getCodpersonal());
                datos[1] = u.getApellido();
                datos[2] = u.getNombre(); 
                datos[3] = u.getTelefono();
                datos[4] = u.getCorreo();
                modelo.addRow(datos);
                TablaUsuarios.setModel(modelo);
            }   
        }
    }
    
