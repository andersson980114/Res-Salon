    private void TablaUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaUsuariosMouseClicked
         // TODO add your handling code here:
        int fila = TablaUsuarios.getSelectedRow();
        Registrar.setEnabled(false);
        Modificar.setEnabled(true);
        Eliminar.setEnabled(true);
        codigo.setEnabled(false);
        Clave.setEnabled(false);
        ConfClave.setEnabled(false);
        Tipo.setEnabled(false);
        Integer documento = Integer.parseInt(TablaUsuarios.getValueAt(fila, 0).toString());
        usuarioEdicion = usuarioLogica.consultarUsuario(documento);
        if (usuarioEdicion != null) {
            Tipo.setSelectedItem(usuarioEdicion.getTipousuario());
            codigo.setText(usuarioEdicion.getCodpersonal().toString());
            Nombre.setText(usuarioEdicion.getNombre());
            Apellido.setText(usuarioEdicion.getApellido());
            Telefono.setText(usuarioEdicion.getTelefono().toString());
            Correo.setText(usuarioEdicion.getCorreo());
            
        }
    }//GEN-LAST:event_TablaUsuariosMouseClicked

    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
         // TODO add your handling code here:
          if (usuarioEdicion != null) {
            try {
                // TODO add your handling code here:
                
                usuarioEdicion.setNombre(Nombre.getText());
                usuarioEdicion.setApellido(Apellido.getText());
                usuarioEdicion.setTelefono(Telefono.getText());
                usuarioEdicion.setCorreo(Correo.getText());
                
                usuarioLogica.modificarUsuario(usuarioEdicion);
                llenarTabla();
                limpiar();
                JOptionPane.showMessageDialog(this, "Usuario actualizado correctamente");
                
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
          
        Registrar.setEnabled(true);
        Modificar.setEnabled(true);
        Eliminar.setEnabled(true);
        codigo.setEnabled(true);
        Clave.setEnabled(true);
        ConfClave.setEnabled(true);
        Tipo.setEnabled(true);
        
    }//GEN-LAST:event_ModificarActionPerformed

    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
         // TODO add your handling code here:
         if (usuarioEdicion != null) {
            int seguro = JOptionPane.showConfirmDialog(this, "Esta seguro que desea borrar el usuario "+usuarioEdicion.getNombre()+" con id "+usuarioEdicion.getCodpersonal()+"?");
            if(seguro==0){
                try {
                    usuarioLogica.eliminarUsuario(usuarioEdicion.getCodpersonal());
                    JOptionPane.showMessageDialog(this, "Usuario eliminado correctamente");
                    limpiar();
                    llenarTabla();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
            }
        }
         Registrar.setEnabled(true);
        Modificar.setEnabled(true);
        Eliminar.setEnabled(true);
        codigo.setEnabled(true);
        Clave.setEnabled(true);
        ConfClave.setEnabled(true);
        Tipo.setEnabled(true);
    }//GEN-LAST:event_EliminarActionPerformed

